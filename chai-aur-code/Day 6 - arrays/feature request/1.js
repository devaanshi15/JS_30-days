// 1. Array Manipulation Script: Write a script that demonstrates the creation of an array, adding and removing elements using push, pop, shift, and unshift methods.

let a = [19,293,11,3,9,10,20,39]
console.log(a)

a.push(100)
console.log(a);

a.pop()
console.log(a);

a.shift()           // Removing the first element using shift
console.log(a);

a.unshift(1)        // Adding an element to the beginning using unshift
console.log(a);